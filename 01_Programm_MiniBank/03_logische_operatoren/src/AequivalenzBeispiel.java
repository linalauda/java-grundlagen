public class AequivalenzBeispiel {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = true;

        // Äquivalenz: Wahr, wenn a und b gleich sind
        boolean ergebnis = (a == b);

        System.out.println("A ist äquivalent zu B: " + ergebnis); // true
    }
}

